public class PrimitiveTypeExamples {
    public static void main(String[] args) {
        // Примеры целочисленных типов
        byte tinyValue = 0;
        short smallValue = 0;
        int mediumValue = 0;
        long largeValue = 0L;

        // Примеры типов с плавающей точкой
        float decimalValue = 0.0f;
        double preciseValue = 0.0;

        // Символьный тип
        char unicodeChar = '\u0000'; // Нулевой символ Unicode

        // Булевский тип
        boolean isTrue = false;

        System.out.println("Примеры базовых типов данных:");
        System.out.println("byte: " + tinyValue);
        System.out.println("short: " + smallValue);
        System.out.println("int: " + mediumValue);
        System.out.println("long: " + largeValue);
        System.out.println("float: " + decimalValue);
        System.out.println("double: " + preciseValue);
        System.out.println("char: " + unicodeChar);
        System.out.println("boolean: " + isTrue);
    }
}
